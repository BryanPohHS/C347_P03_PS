package com.bryanpoh.c347_p03_ps;

public class DailyGrade {
    private String grade;
    private String week;

    public DailyGrade(String grade, String week) {
        this.grade = grade;
        this.week = week;
    }

    public String getGrade() {
        return grade;
    }

    public String getWeek() {
        return week;
    }
}
